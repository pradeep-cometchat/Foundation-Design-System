/**
 * Build the font-variation-settings string for Material Symbols axes.
 */
export const buildIconVariation = (opts: {
  fill?: 0 | 1;
  weight?: number;
  grade?: number;
  opticalSize?: number;
}): string => {
  const fill = opts.fill ?? 0;
  const wght = opts.weight ?? 400;
  const grad = opts.grade ?? 0;
  const opsz = opts.opticalSize ?? 24;
  return `'FILL' ${fill}, 'wght' ${wght}, 'GRAD' ${grad}, 'opsz' ${opsz}`;
};

/**
 * Material Symbols — Google's modern Material Icons.
 *
 * One variable font powers every icon. Four axes control appearance:
 *   - FILL:   0 (outline) → 1 (filled)
 *   - wght:   100 → 700 (thickness)
 *   - GRAD:   -25 → 200 (emphasis)
 *   - opsz:   20 → 48 (optical size)
 *
 * Three style variants are available: Outlined, Rounded, Sharp.
 *
 * To use an icon, render its *ligature name* as text inside a span with
 * the appropriate font-family. See the Icon component for a typed wrapper.
 */

